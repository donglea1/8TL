   QLNVDAO qlnvdao = new QLNVDAO();

    void dangNhap() {
        String tk = txtTK.getText();
        String matKhau = txtPass.getText();
        EMPLOYEE epl = qlnvdao.selectebyID(tk);
        if (epl == null) {
            JOptionPane.showMessageDialog(this, "Wrong username!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!matKhau.equals(epl.getMATKHAU())) {
            JOptionPane.showMessageDialog(this, "Wrong password!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Auth.user = epl;
            MsgBox.alert(this, "Login successfully!");
            this.dispose();
        }
    }

