    boolean isHidePass = true;
    File f = new File("");
    private void showPass() {
        String duongdanEmployee = "\\src\\main\\resources\\com\\qlbh\\icon\\Employee\\";
        if (isHidePass) {
            txtPass.setEchoChar((char) 0);
            btn_Show.setIcon(new ImageIcon(f.getAbsolutePath() + duongdanEmployee + "icons8_eye_25px.png"));
            isHidePass = false;
        } else {
            txtPass.setEchoChar('*');
            btn_Show.setIcon(new ImageIcon(f.getAbsolutePath() + duongdanEmployee + "icons8_hide_25px.png"));
            isHidePass = true;
        }
    }
    
