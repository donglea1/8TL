    String duongDanImage = null;

    private void chooseImage() {
        duongDanImage = f.getAbsolutePath() + "\\src\\main\\resources\\com\\qlbh\\icon\\Employee\\ImageEmployee";
        try {
            JFileChooser fc = new JFileChooser(f.getAbsolutePath() + "\\src\\main\\resources\\com\\qlbh\\icon\\Employee\\ImageEmployee");
            fc.setDialogTitle("Open File");
            fc.showOpenDialog(null);

            File fTenAnh = fc.getSelectedFile();
            duongDanImage = fTenAnh.getAbsolutePath();
            if (duongDanImage != null) {
                lbl_EditInfo_Image.setIcon(ResizeImage(lbl_EditInfo_Image.getWidth(), lbl_EditInfo_Image.getHeight(), String.valueOf(duongDanImage)));
                System.out.println(duongDanImage);
            } else {
                System.out.println("Image not selected!");
            }
        } catch (Exception e) {
            System.out.println("Image not selected!");
            System.out.println(duongDanImage);
        }
    }
    
