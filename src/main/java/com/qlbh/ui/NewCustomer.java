    private boolean validateForm() {
        String Error = "";
        if (txt_CUSMANA_MAKH.getText().equals("")) {
            Error += "ID Employee not null! \n";
        }
       
        if (txt_CUSMANA_TENKH.getText().equalsIgnoreCase("")) {
            Error += "Fullname not null! \n";
        }
        String reg = "^(0|\\+84)(\\s|\\.)?((3[2-9])|(5[689])|(7[06-9])|(8[1-689])|(9[0-46-9]))(\\d)(\\s|\\.)?(\\d{3})(\\s|\\.)?(\\d{3})$";
        if (txt_CUSMANA_PHONE.getText().equals("")) {
            Error += "Phone not null! \n";
        } else if (!txt_CUSMANA_PHONE.getText().matches(reg)) {
            Error += "phone is not in the correct format! \n";
        }
        if (txt_CUSMANA_DIACHI.getText().equals("")) {
            Error += "Address not null! \n";
        }
        String EMAIL_PATTERN = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        if (txt_CUSMANA_EMAIL.getText().equals("")) {
            Error += "Gmail not null! \n";
        } else if (!txt_CUSMANA_EMAIL.getText().matches(EMAIL_PATTERN)) {
            Error += "Gmail is not in the correct format! \n";
        }
       
        if (!Error.equals("")) {
            JOptionPane.showMessageDialog(this, Error, "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
