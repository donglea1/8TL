    public void deleteEmployee() {
        QLNVDAO qlnvdao = new QLNVDAO();
        NewEmployee ne = new NewEmployee();
        if (getInfo.employee != null) {
            if (!Auth.user.getMANV().equals(getInfo.employee.getMANV())) {
                if (qlnvdao.selectebyID(getInfo.employee.getMANV()) != null) {
                    qlnvdao.delete(getInfo.employee.getMANV());
                    MsgBox.alert(this, "Delete successfully!");
                    getInfo.employee = null;
                    this.dispose();
                    ne.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Employee doesn't exist!", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "you can't delete yourself!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Employee doesn't exist!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void btn_WarningInventory_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_WarningInventory_okActionPerformed
        EMPLOYEE epl = Auth.user;
        if (epl != null) {
            if (epl.getMATKHAU().equals(txt_WarningInventory_pass.getText())) {
                // Gọi hàm delete ở đây
                deleteEmployee();
            } else {
                JOptionPane.showMessageDialog(this, "confirm password wrong!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_WarningInventory_okActionPerformed

